#include <bits/stdc++.h>
using namespace std;
// #define int short

string s;
int n;

void solve()
{
    cin >> n;
    cin >> s;
    s = " " + s;

    vector<int> pre(n + 1, 0);

    for (int i = 1; i <= n; i++)
    {
        pre[i] = pre[i - 1] + (s[i] == '1');
    }

    auto cal = [&](int l, int r, int c) -> int
    {
        if (l > r) return 0;

        int one = pre[r] - pre[l - 1];
        int len = r - l + 1;

        if (c == 1) return len - one;
        else return one;
    };

    int ans = n;

    for (int k = n; k >= 3; k--)
    {
        for (int i = 2; i <= k - 2; i++)
        {
            for (int j = i; j <= k - 2; j++)
            {
                int cnt = 0;

                cnt += cal(1, i - 1, 0);
                cnt += cal(i, j, 1);
                cnt += cal(j + 1, k - 1, 0);
                cnt += cal(k, n, 1);

                ans = min(ans, cnt);

                ans = min(ans, n - cnt);
            }
        }
    }

    cout << ans << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    solve();

    return 0;
}

